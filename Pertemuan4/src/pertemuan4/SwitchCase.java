public class SwitchCase {
    
    public static void main(String[] args) {
        
        int nilai = 7;
        switch(nilai){
            case 0:
                System.out.println("Anda memilih 0");
                break;
            case 1:
                System.out.println("Anda memilih 1");
                break; 
            case 2:
                System.out.println("Anda memilih 2");
                break;
            case 3:
                System.out.println("Anda memilih 3");
                break;   
            case 4:
                System.out.println("Anda memilih 4");
                break;
            case 5:
                System.out.println("Anda memilih 5");
                break;
            case 6:
                System.out.println("Anda memilih 6");
                break;
            case 7:
                System.out.println("Anda memilih 7");
                break;
            default:
                System.out.println("Tidak dapat ditemukan");
        }
    }
}
